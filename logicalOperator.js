

// logical operator
// 1. AND (&&)
// 2. OR (||)
// 3. NOT (!)


// AND (&&)
// if left side is true, then return right side
const money = 100;
console.log(money && 'I have money'); 

/*


*/
// OR (||)
// if left side is false, then return right side
const name = '';
console.log(name || 'Anonymous');

/*


*/
// NOT (!)
// if true, then return false
const isTrue = true;
console.log(!isTrue);

